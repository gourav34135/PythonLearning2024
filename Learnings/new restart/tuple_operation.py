# Creating a tuple
my_tuple = (1, 2, 3, 4, 5)
# elements in a tuple
print("Tuple elements:", my_tuple)
#basic operation are aws follows
#Length of the tuple
tuple_length = len(my_tuple)
print("Length of the tuple:", tuple_length)

#Sum of all elements in the tuple
tuple_sum = sum(my_tuple)
print("Sum of the tuple elements:", tuple_sum)

#Finding the minimum and maximum values in the tuple
tuple_min = min(my_tuple)
tuple_max = max(my_tuple)
print("Minimum value in the tuple:", tuple_min)
print("Maximum value in the tuple:", tuple_max)
